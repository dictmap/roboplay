def write(path: Path, content: str, force: bool) -> None:
    if path.exists() and not force:
        raise FileExistsError(f"{path} already exists; pass --force to overwrite")
    path.write_text(content, encoding="utf-8")
    print(f"wrote {path}")


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--robolab-root", type=Path, default=Path.cwd())
    parser.add_argument("--force", action="store_true")
    args = parser.parse_args()

    root = args.robolab_root.resolve()
    policy_dir = root / "policies" / "pi0_family"
    if not (policy_dir / "client.py").exists():
        raise FileNotFoundError(f"RoboLab Pi0-family client not found under {policy_dir}")
    write(policy_dir / "object_position_events.py", EVENTS_CODE, args.force)
    write(policy_dir / "run_object_position_variation.py", RUNNER_CODE, args.force)


if __name__ == "__main__":
    main()
