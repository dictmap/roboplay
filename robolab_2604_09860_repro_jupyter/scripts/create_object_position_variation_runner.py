EVENTS_CODE = r'''# Auto-generated by create_object_position_variation_runner.py.
import torch
import isaaclab.utils.math as math_utils
from isaaclab.envs import ManagerBasedEnv


def reset_named_object_pose_uniform(
    env: ManagerBasedEnv,
    env_ids: torch.Tensor,
    object_names: list[str],
    pose_range: dict[str, tuple[float, float]],
    velocity_range: dict[str, tuple[float, float]] | None = None,
):
    """Randomize named rigid object root poses around their default poses."""
    velocity_range = velocity_range or {}
    for object_name in object_names:
        if object_name not in env.scene.rigid_objects:
            print(f"[object-position-variation] skip missing rigid object: {object_name}")
            continue
        asset = env.scene.rigid_objects[object_name]
        root_states = asset.data.default_root_state[env_ids].clone()

        pose_ranges = torch.tensor(
            [pose_range.get(key, (0.0, 0.0)) for key in ["x", "y", "z", "roll", "pitch", "yaw"]],
            device=asset.device,
        )
        samples = math_utils.sample_uniform(pose_ranges[:, 0], pose_ranges[:, 1], (len(env_ids), 6), device=asset.device)
        positions = root_states[:, 0:3] + env.scene.env_origins[env_ids] + samples[:, 0:3]
        orientation_delta = math_utils.quat_from_euler_xyz(samples[:, 3], samples[:, 4], samples[:, 5])
        orientations = math_utils.quat_mul(root_states[:, 3:7], orientation_delta)

        velocity_ranges = torch.tensor(
            [velocity_range.get(key, (0.0, 0.0)) for key in ["x", "y", "z", "roll", "pitch", "yaw"]],
            device=asset.device,
        )
        velocity_samples = math_utils.sample_uniform(
            velocity_ranges[:, 0], velocity_ranges[:, 1], (len(env_ids), 6), device=asset.device
        )
        velocities = root_states[:, 7:13] + velocity_samples

        asset.write_root_pose_to_sim(torch.cat([positions, orientations], dim=-1), env_ids=env_ids)
        asset.write_root_velocity_to_sim(velocities, env_ids=env_ids)
'''


